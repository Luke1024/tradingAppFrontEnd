package frontend.chartDrawer.utilities.processor.utilities.utilities;

public class Rectangle {
    private Color color;
    private int x;
    private int y;
    private int width;
    private int height;

    public Rectangle(Color color, int x, int y, int width, int height) {
        this.color = color;
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }

    public Color getColor() {
        return color;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
